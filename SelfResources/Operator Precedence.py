## Operator Precedence in Python - from highest to lowest precedence

## Operator                          ## Description
## **                                   Exponentiation (2 ^ 2)
## ~, +, -                              Complement, unary plus/minus
## *, /, %, //                          Multiply, divide, modulo, floor division
## +, -                                 Addition & Subtraction
## >>, <<                               Right & left bitwise shift
## &                                    Bitwise 'AND'
## ^                                    Bitwise 'XOR'
## |                                    Bitwise 'OR'
## in, not in, is, is not, <, >         Comparison operators, equality operators
## <=, >=, !=, ==                       membership & identity operators
## not                                  Boolean 'NOT'
## and                                  Boolean 'AND'
## or                                   Boolean 'OR'
## =, %=, /=, //=, -=, +=, *=, **=      Assignment operators