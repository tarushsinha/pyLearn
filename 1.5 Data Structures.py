## Data Structures - When to Use!

## As seen in previous sections, Python has support for the following data structures: lists, dictionaries, tuples, sets

## USAGE:

## When to use a dictionary:
## -> When in need of a logical association between a key:value pair
## -> When you need fast lookup for your data, based on a custom key.
## -> When your data is being constantly modified. Remember, dictionaries are mutable

## When to use lists:
## -> If you have a collection of data that does not need random access
## -> Try to choose lists as a data structure when in need of a simple iterable collection, modified frequently

## When to use sets:
## -> When you require uniqueness of elements, use a Set

## When to use tuples:
## -> Use tuples when data cannot change (immutable data)
## -> **Many times, tuples are used in combination with a dictionary, for example a tuple might represent a key**

